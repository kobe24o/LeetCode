void print2Dvector(vector<vector<int>>& a)
{
    int m = a.size(), n = a[0].size();
    for(int i = 0; i < m; ++i)
    {
        for(int j = 0; j < n; ++j)
            cout << a[i][j] << " ";
        cout << endl;
    }
}

class Solution {
public:
    int findKthNumber(int m, int n, int k) {
        int l = 1, r = m*n, mid, ans;
        while(l <= r)
        {
            mid = l+((r-l)>>1);
            if(ok(m,n,mid,k))
            {
                ans = mid;
                r = mid-1;
            }
            else
                l = mid+1;
        }
        return ans;
    }
    bool ok(int m, int n, int mid, int k)
    {	// 检查每一行，<= mid 的数，共计有多少个
        int count = 0;
        for(int i = 1; i <= m; i++)
        {
            count += mid/i;
        }
        return count >= k;
    }
};


